    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        String user = txtCPF.getText();
        Profi obj = new Profi();
        ProfisDAO dao = new ProfisDAO();
        obj = dao.BuscarProfTudo(user);
        if(obj.getCPF()!= null){

            obj.setNome(txtNome.getText());
            obj.setDataNascimento(txtDataNascimento.getText());
            obj.setCartaoSUS(txtCartaoSUS.getText());
            obj.setCPF(txtCPF.getText());
            obj.setFuncao(txtCargo.getSelectedItem().toString());
            obj.setIDespecialidade(txtEspecialidade.getSelectedItem().toString());
            obj.setSalario(Float.parseFloat(txtSalario.getText()));
            obj.setIDCredencial(txtConselho.getSelectedItem().toString());
            obj.setIDProf(Integer.valueOf(txtID.getText()));
            if (txtSituaçao.isSelected()) {  
                int situacao = 0;
                obj.setSituacao(situacao);
            
            }else{
                int situacao = 1;
                obj.setSituacao(situacao);}
            
                dao.Editar(obj);
        
        }else{          
            obj.setNome(txtNome.getText());
            obj.setDataNascimento(txtDataNascimento.getText());
            obj.setCartaoSUS(txtCartaoSUS.getText());
            obj.setCPF(txtCPF.getText());
            obj.setFuncao(txtCargo.getSelectedItem().toString());
            obj.setIDespecialidade(txtEspecialidade.getSelectedItem().toString());
            obj.setSalario(Float.parseFloat(txtSalario.getText()));
            obj.setIDCredencial(txtConselho.getSelectedItem().toString());
            dao.Salvar(obj);
            
            }
    }//GEN-LAST:event_jButton2ActionPerformed

