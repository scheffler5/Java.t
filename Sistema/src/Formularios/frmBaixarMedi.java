    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        EstoqueMediDAO dao = new EstoqueMediDAO();
        DefaultTableModel dados = (DefaultTableModel) Tabela.getModel();

try {
    for (int i = 0; i < dados.getRowCount(); i++) {
        if (dados.getValueAt(i, 0) != null) {
            try {
                EstoqueMedi obj = new EstoqueMedi();
                obj.setCDC(Integer.parseInt(txtCOD.getText()));

                
                // Obtém o ID do medicamento
                String valorCelula = dados.getValueAt(i, 1).toString();
                int medicamento = Integer.parseInt(valorCelula);
                obj.setMedicamentos(medicamento);


                dao.SalvarBaixa(obj);
            } catch (NumberFormatException e) {
                JOptionPane.showMessageDialog(null, "Erro na linha " + (i + 1) + ": O valor não é um número válido.");
            }
        }
    }
    JOptionPane.showMessageDialog(null, "Todos os lançamentos foram realizados com sucesso!");
} catch (Exception e) {
    JOptionPane.showMessageDialog(null, "Erro ao processar a tabela: " + e.getMessage());
}
        


    int Liberacao = 1;
    PrescricaoModel obj = new PrescricaoModel();
    PrescricaoModelDAO DAO = new PrescricaoModelDAO();
    obj = DAO.BuscarBaixa(Integer.parseInt(txtPresc.getText()));
    obj.setProntuario(Integer.parseInt(txtPresc.getText()));
    obj.setBaixada(Integer.valueOf(Liberacao));
    DAO.Editar(obj);

    }//GEN-LAST:event_jButton2ActionPerformed

