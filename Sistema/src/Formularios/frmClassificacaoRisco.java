    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        Prontuario obv = new Prontuario();
        ProntuarioDAO DAO = new ProntuarioDAO();
        int indice = txtRisco.getSelectedIndex();
        if (indice == 0) {
            JOptionPane.showMessageDialog(null, "Por favor, selecione uma classificação válida.");
            return;
        }
        int idClassificacao = 0;
        if (indice == 1) {
            idClassificacao = 1; 
        } else if (indice == 2) {
            idClassificacao = 2; 
        } else if (indice == 3) {
            idClassificacao = 3; 
        } else if (indice == 4) {
            idClassificacao = 4; 
        }
        obv.setClassificacaoRisco(idClassificacao); 
        obv.setProntuario(Integer.valueOf(txtPront.getText())); 
        DAO.EditarClassificacao(obv);
        
        RespClassificacao obj = new RespClassificacao();
        RespClassiciacaoDAO dao = new RespClassiciacaoDAO();

            obj.setProntuario_ID(Integer.valueOf(txtPront.getText()));
            obj.setAltura(txtAltura.getText());
            obj.setBatimentos(txtBatimento.getText());
            obj.setGlicose(txtGlicose.getText());
            obj.setIMC(txtIMC.getText());
            obj.setPeso(txtPeso.getText());
            obj.setPressao(txtPressao.getText());
            obj.setSintomas(txtSintomas.getText());
            obj.setTemperatura(txtTemperatura.getText());
            dao.Salvar(obj);
            
            txtAltura.setText("");
            txtBatimento.setText("");
            txtGlicose.setText("");
            txtIMC.setText("");
            txtNome.setText("");
            txtPeso.setText("");
            txtPressao.setText("");
            txtPront.setText("");
            txtSintomas.setText("");
            txtTemperatura.setText("");
            txtRisco.setSelectedIndex(0);
            
            
    }//GEN-LAST:event_jButton1ActionPerformed

